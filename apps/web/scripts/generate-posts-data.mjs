import fs from 'fs';
import path from 'path';
import matter from 'gray-matter';
import readingTime from 'reading-time';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const postsDirectory = path.join(__dirname, '../content/blog/posts');
const outputPath = path.join(__dirname, '../lib/blog/posts-data.json');

function getAllPostsData() {
  if (!fs.existsSync(postsDirectory)) {
    console.warn('Posts directory does not exist:', postsDirectory);
    return [];
  }

  const fileNames = fs.readdirSync(postsDirectory);
  const posts = fileNames
    .filter(fileName => fileName.endsWith('.md'))
    .map(fileName => {
      const slug = fileName.replace(/\.md$/, '');
      const filePath = path.join(postsDirectory, fileName);
      const fileContents = fs.readFileSync(filePath, 'utf8');
      const { data, content } = matter(fileContents);
      const { text: readingTimeText } = readingTime(content);

      return {
        slug,
        frontmatter: {
          ...data,
          readingTime: readingTimeText,
        },
        content,
        readingTime: readingTimeText,
      };
    })
    .sort((a, b) =>
      new Date(b.frontmatter.publishedAt).getTime() -
      new Date(a.frontmatter.publishedAt).getTime()
    );

  return posts;
}

const posts = getAllPostsData();

// Generate TypeScript file instead of JSON for better bundling
const tsOutputPath = path.join(__dirname, '../lib/blog/posts-data.ts');
const tsContent = `// Auto-generated file. Do not edit manually.
// Generated by scripts/generate-posts-data.mjs

export const postsData = ${JSON.stringify(posts, null, 2)} as const;
`;

fs.writeFileSync(tsOutputPath, tsContent);
console.log(`âœ… Generated posts data: ${posts.length} posts written to ${tsOutputPath}`);
